class Solve {
    int[] findTwoElement(int arr[]) {
        // code here
         int n=arr.length;
        int[] res=new int[2];
        HashMap<Integer,Integer> map=new HashMap<>();
        for(int i=0;i<n;i++){
            map.put(arr[i],map.getOrDefault(arr[i],0)+1);
        }
        for(int i=1;i<=n;i++){
            if(map.get(arr[i-1])>1) res[0]=arr[i-1];
            if(!map.containsKey(i)) res[1]=i;
            
        }
        return res;
    }
}